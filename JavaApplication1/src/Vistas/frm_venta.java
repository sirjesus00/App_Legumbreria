    private void fnt_calcular(double precio, int cantidad, int categoria){
        
        int index = categoria;
        double nuevo_precio;
        double descuento;
        String nuevo_valor = "";
        switch(index){
        
            case 0:
                descuento= (cantidad*precio)*10/100;
                nuevo_precio=(cantidad*precio)-descuento;
                nuevo_valor =String.valueOf(nuevo_precio);
                txt_total.setText(nuevo_valor);
                break;
        
                
            case 1:
                descuento= (cantidad*precio)*5/100;
                nuevo_precio=(cantidad*precio)-descuento;
                nuevo_valor =String.valueOf(nuevo_precio);
                txt_total.setText(nuevo_valor);
                break;
                
                
            case 2:
                descuento= (cantidad*precio)*3/100;
                nuevo_precio=(cantidad*precio)-descuento;
                nuevo_valor =String.valueOf(nuevo_precio);
                txt_total.setText(nuevo_valor);
                break;
                
                
            case 3:
                descuento= (cantidad*precio)*20/100;
                nuevo_precio=(cantidad*precio)-descuento;
                nuevo_valor =String.valueOf(nuevo_precio);
                txt_total.setText(nuevo_valor);
                break;
                
                
            default:
                break;
        }
    
    }   
    
