    private void fnt_vender(){
        
        double precioDou = Double.parseDouble(txt_precio.getText());
        double totalDou = Double.parseDouble(txt_total.getText());
        int cantidadInt = Integer.parseInt(txt_cantidad.getText());
        String categoriaStr = (String) cbx_categoria.getSelectedItem();
        int id_UsuarioInt = Integer.parseInt(txt_idUsuario.getText());
        String nombre_productoStr = txt_producto.getText();
        cls_venta v1 = new cls_venta(id_UsuarioInt, categoriaStr, nombre_productoStr, precioDou, cantidadInt, totalDou);
        ventas.add(v1);
        JOptionPane.showMessageDialog(null, "Venta registrada con exito");
        posInt +=1;
    }
    
